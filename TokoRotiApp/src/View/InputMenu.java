    private void loadData() {
    String[] judul = {"ID", "Nama Roti", "Harga", "Stok", "Kategori", "Image Path"};
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(null, judul);
    
    java.util.List<Model.Product> listRoti = controller.ambilSemuaData();
    
    for (Model.Product p : listRoti) {
        Object[] row = new Object[6];
        row[0] = p.getId();
        row[1] = p.getName();
        row[2] = p.getPrice();
        row[3] = p.getStock();
        row[4] = p.getCategory();
        row[5] = p.getImagePath();
        model.addRow(row);
    }
    
    tblRoti.setModel(model);
    
    tblRoti.getColumnModel().getColumn(5).setMinWidth(0);
    tblRoti.getColumnModel().getColumn(5).setMaxWidth(0);
    tblRoti.getColumnModel().getColumn(5).setWidth(0);
    }

        private void GambarLogo(javax.swing.JLabel label, String resourcePath) {
        ImageIcon imgIco = new ImageIcon(
            getClass().getResource(resourcePath)
        );
        
        Image image = imgIco.getImage().getScaledInstance(
                label.getWidth(),
                label.getHeight(),
                Image.SCALE_SMOOTH
        );

        label.setIcon(new ImageIcon(image));
    }
        
        private void TempatFoto(javax.swing.JLabel label, File file) {
    try {
        BufferedImage img = ImageIO.read(file);
        if (img == null) {
            JOptionPane.showMessageDialog(this, "File bukan gambar.");
            return;
        }

        int lw = label.getWidth();
        int lh = label.getHeight();
        double s = Math.min((double) lw / img.getWidth(), (double) lh / img.getHeight());
        int nw = Math.max(1, (int) Math.round(img.getWidth()  * s));
        int nh = Math.max(1, (int) Math.round(img.getHeight() * s));

        Image scaled = img.getScaledInstance(nw, nh, Image.SCALE_SMOOTH);
        label.setIcon(new ImageIcon(scaled));
        label.setText(null);
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Gagal baca gambar: " + e.getMessage());
    }
}

