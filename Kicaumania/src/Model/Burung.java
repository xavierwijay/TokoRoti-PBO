package Model;
public class Burung {
    private int id;
    private String namaPemilik;
    private String namaBurung;
    private String jenisBurung;
    private String nomorGantungan;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNamaPemilik() {
        return namaPemilik;
    }

    public void setNamaPemilik(String namaPemilik) {
        this.namaPemilik = namaPemilik;
    }

    public String getNamaBurung() {
        return namaBurung;
    }

    public void setNamaBurung(String namaBurung) {
        this.namaBurung = namaBurung;
    }

    public String getJenisBurung() {
        return jenisBurung;
    }

    public void setJenisBurung(String jenisBurung) {
        this.jenisBurung = jenisBurung;
    }

    public String getNomorGantungan() {
        return nomorGantungan;
    }

    public void setNomorGantungan(String nomorGantungan) {
        this.nomorGantungan = nomorGantungan;
    }

   
}